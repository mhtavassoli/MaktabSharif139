import pandas as pd
import numpy as np
array1d = np.array([2, 5, 7, 9, 12, 15])
print(f"The 1D Array is: {array1d}")
sum_array1D=np.sum(array1d)
print(f"The sum of array is: {sum_array1D}")
mean_array1D=np.mean(array1d)
print(f"The mean of array is: {mean_array1D}")
max_array1D=np.max(array1d)
print(f"The maximum of array is: {max_array1D}")
min_array1D=np.min(array1d)
print(f"The minimum of array is: {min_array1D}")
